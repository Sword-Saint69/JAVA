package Expression_Blocks;

public class UnaryOperator {
    public static void main(String[] args) {

        System.out.println("Unary Operator");

        int a = 5,b = 4;

        int newvalue = a++;
        System.out.println(newvalue);

        System.out.println(a);

        System.out.println(++b);


    }
}
