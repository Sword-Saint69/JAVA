package IntoTopic;

public class DatatypesDemo {

    public static void main(String[] args) {

        int Speedlimit = 29;
        System.out.println(Speedlimit);

        byte SpeedLimit = 23;
        System.out.println(SpeedLimit);

        long speedlimit = 24;
        System.out.println(speedlimit);

        short Speedlimit2 = 13;
        System.out.println(Speedlimit2);

        float Speedlimit3 = 13.5f;
        System.out.println(Speedlimit3);

        double Speedlimit4 = 13.5;
        System.out.println(Speedlimit4);

        char Speedlimit5 = 13;
        System.out.println(Speedlimit5);

        boolean Speedlimit6 = true;
        System.out.println(Speedlimit6);


    }
}
