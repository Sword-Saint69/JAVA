package IntoTopic;

public class printdemo {

    public static void main(String[] args) {

//First Way to print
        System.out.println("1.print");
        System.out.println("2.print");

//Second Way to print
        System.out.print("1.print ");
        System.out.print("2.print");

//blank line
        System.out.println();

//Third way to print
        System.out.printf("1.print %d!%n",100);
        System.out.printf("2.print %d!%n",10);
    }
}
